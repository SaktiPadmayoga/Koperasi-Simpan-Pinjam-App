    private void savingsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_savingsTableMouseClicked
        isSavingsTableClicked = true;
        noAkunInput.setEnabled(false);
        okSavingBtn.setEnabled(false);
        jumlahSavingInput.setEnabled(false);
        cbSavingType.setEnabled(false);
        setRadio(false);
        
        int clickedRow = savingsTable.getSelectedRow();
        TableModel tableModel = savingsTable.getModel();
        
        noAkunInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
        Accounts p = aControl.searchAccount(Integer.parseInt((noAkunInput.getText())));
        namaMemberInput.setText(p.getMember().getFirst_name()+" "+p.getMember().getLast_name());
        jumlahSavingInput.setText(tableModel.getValueAt(clickedRow, 5).toString());
        idSavingInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        cbSavingType.setSelectedItem(tableModel.getValueAt(clickedRow, 2).toString());
        savingBtn.setEnabled(false);
        String radio = tableModel.getValueAt(clickedRow, 6).toString();
        switch(radio){
            case "BRI" :
                p1.setSelected(true);
                break;
            case "BNI" :
                p2.setSelected(true);
                break;
            case "DANA":
                p3.setSelected(true);
                break;
            case "OVO":
                p4.setSelected(true);
                break;
            case "Tunai":
                p5.setSelected(true);
                break;
        }
        isSavingsTableClicked = false;
    }//GEN-LAST:event_savingsTableMouseClicked

