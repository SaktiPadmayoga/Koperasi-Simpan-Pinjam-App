    private void savingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savingBtnActionPerformed
        try{
            blankInputException();
            int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Are you sure to saving?", "Confirmation",JOptionPane.YES_NO_OPTION);
            switch (getAnswer) {
                case 0:
                transactionControl.insertTransaction(new Transactions("SV-"+String.valueOf(ThreadLocalRandom.current().nextInt(0, 99999)),
                    Integer.parseInt(noAkunInput.getText()), idSavingInput.getText(), LocalDate.now().toString()));
                savingControl.insertSavings(new Savings(idSavingInput.getText(), cbSavingType.getSelectedItem().toString(), LocalDate.now().toString(), payGrp.getSelection().toString(), Double.parseDouble(jumlahSavingInput.getText()), Integer.parseInt(noAkunInput.getText())));
        JOptionPane.showMessageDialog(null, "Saving success !");
        clearText();
        break;

        case 1:
        //do nothing
        break;
        default:
        throw new AssertionError();
        }
        }catch(BlankInputException e){
            JOptionPane.showConfirmDialog(null, e.message(), "Warning", JOptionPane.DEFAULT_OPTION);
            System.out.println("Error: " + e.toString());
        }catch(NumberFormatException e){
            JOptionPane.showConfirmDialog(null, "Amount Saving Must Be Number ! ", "Warning", JOptionPane.DEFAULT_OPTION);
            System.out.println("Error: " + e.toString());
        }
        showSaving();
    }//GEN-LAST:event_savingBtnActionPerformed

